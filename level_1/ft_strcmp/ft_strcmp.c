#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	res;

	i = 0;
	res = 0;
	while (s1[i])
	{
		res = s1[i] - s2[i];
		i++;
	}
	return (res);
}